def timeDiff(TA, TZ=None):
    """
    show me how much time we take
    """
    if TZ is None:
        TZ = DT.datetime.now()
    return TZ - TA
    print('-'.rjust(60, '-'), flush=True)


def next_Prim(L):
    """
    next prime - first JumpList-element added to 1
    """
    return 1 + L[0]


def gen_Numbers(f, L):
    """
    compute a couple of numbers based on current JumpList = differences
    """
    t = 1
    while True:
        for k in L:
            t += k
            yield t
            # endless if no edge given
            if (f <= 0):
                continue
            # but let it end if running sum runs over given edge
            if (t >= f):
                return None


def gen_Strikes(L):
    """
    get the numbers to be removed from JumpList - as like in E-sieve
    """
    p = next_Prim(L)
    t = 1
    yield t*p
    for k in L:
        t += k
        yield t*p


def gen_Jumpers(L):
    """
    compute the next list of differences based on the current one
    """
    ta = 1
    tz = ta
    p = next_Prim(L)
    S = gen_Strikes(L)
    s = next(S)
    for i in range(p):
        for k in L:
            tz += k
            if tz == s:
                s = next(S)
                continue
            yield tz - ta
            ta = tz


def gen_Primes(pp, K, Q, L):
    """
    generate more primes by repeating the JumpList til MaxNumber
    """
    global MAX_PRIME
    global MAX_NUMBER
    global cnt_divisions
    global cnt_no_primes
    global cnt_compares
    global cnt_numbers
    global dict_P_runlen
    global dict_N_runlen
    # get the primes by testing the remainder
    # after division with formerly computed primes
    # stop when found square of prime greater current number
    cnt_divisions = 0
    cnt_no_primes = 0
    cnt_compares = 0
    cnt_numbers = 0
    X = gen_Numbers(0, L)
    # we already know some primes - skip them
    while True:
        x = next(X)
        if x >= pp:  # also ignore pp - it's a square
            break  # we are at the edge
    # now compare numbers revers against squares - think about roots
    # and test the remainder ...
    while True:
        x = next(X)
        if x > MAX_NUMBER:
            break
        cnt_numbers += 1
        prime_found = True  # assume tha's a prime
        cnt_runlength = 0
        for p, q in zip(K, Q):
            cnt_compares += 1
            if q > x:
                # compare current x against the sqare of known primes
                # if gretaer than we need no more divisors from list
                break
            cnt_runlength += 1
            cnt_divisions += 1
            if x % p == 0:
                # primitive test for a prime
                prime_found = False  # wrong assumptions - get next x
                cnt_no_primes += 1
                break
        else:
            prime_found = False
            assert False, "PrimeDivisorError/SquareRuleError"
        if prime_found:
            # ok - we found one - also store new values for future compares
            K.append(x)
            Q.append(x*x)
            dict_P_runlen[cnt_runlength] = dict_P_runlen.get(
                    cnt_runlength, 0) + 1
            yield x
        else:
            dict_N_runlen[cnt_runlength] = dict_N_runlen.get(
                    cnt_runlength, 0) + 1
    return


