def next_Prim(L):
    """
    next prime - first JumpList-element added to 1
    """
    return 1 + L[0]


def gen_Numbers(f, L):
    """
    compute a number based on current JumpList = differences
    """
    t = 1
    while True:
        for k in L:
            t += k
            yield t
            # endless if no edge given
            if (f <= 0):
                continue
            # but let end if running sum runs over current edge
            if (t >= f):
                return None


def gen_Strikes(L):
    """
    get the numbers to be removed from JumpList - as like in E-sieve
    """
    p = next_Prim(L)
    t = 1
    yield t*p
    for k in L:
        t += k
        yield t*p


def gen_Jumpers(L):
    """
    compute the next list of differences based on current one
    """
    ta = 1
    tz = ta
    p = next_Prim(L)
    S = gen_Strikes(L)
    s = next(S)
#    ma = 0
#    mz = ma
    for i in range(p):
        for k in L:
            tz += k
#            if SHOW_PROGRESS:
#                mz = (tz >> 20)
#                if (mz > ma):
#                    print(f'\r{tz:15,}\r', end="", flush=True)
#                    ma = mz
            if tz == s:
                s = next(S)
                continue
            yield tz - ta
            ta = tz


def gen_Primes(pp, K, Q, L):
    """
    generate more primes by repeating the JumpList til MaxNumber
    """
    global MAX_PRIME
    global MAX_NUMBER
    global cnt_divisions
    global cnt_runlength
    global cnt_no_primes
    global dict_P_runlen
    global dict_N_runlen
    # get the primes by testing the remainder
    # after division with formerly computed primes
    # stop when found square of prime greater testet number
    # measure some counts while computing the new primes
    cnt_divisions = 0
    cnt_no_primes = 0
    X = gen_Numbers(0, L)
    # we already know some primes - skip them
    while True:
        x = next(X)
        if x >= pp:
            break  # we are at the edge
    # but ignore pp - it's a square
    while True:
        x = next(X)
        # print("<", x, end=">  ")
        if x > MAX_NUMBER:
            break
        prime_found = True  # assume tha's a prime
        cnt_runlength = 0
        for p, q in zip(K, Q):
            # print("\t##", p, q, "## ", end="")
            if q > x:
                # we need no more divisors
                # print("\t**", q, end="\n")
                break
            cnt_divisions += 1
            cnt_runlength += 1
            if x % p == 0:
                # primitive test for a prime
                # print("\t!!", p, end="\n")
                prime_found = False  # wrong assumpions - get next x
                cnt_no_primes += 1
                break
        if prime_found:
            # ok - we found one - store needed values
            # print("\tOK", end="\n")
            if EVAL_RUNLENGTH:
                dict_P_runlen[cnt_runlength] = dict_P_runlen.get(
                        cnt_runlength, 0) + 1
            K.append(x)
            Q.append(x*x)
            yield x
        else:
            if EVAL_RUNLENGTH:
                dict_N_runlen[cnt_runlength] = dict_N_runlen.get(
                        cnt_runlength, 0) + 1
    return


